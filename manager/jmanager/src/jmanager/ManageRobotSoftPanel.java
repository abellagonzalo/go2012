    private void initRobotIp() {
         File file = new File(ROBOTS_IP_CONF_FILE);

        try {

            BufferedReader bis = new BufferedReader(new FileReader(file));

            RobotIPTextField1.setText(bis.readLine());
            RobotIPTextField2.setText(bis.readLine());
            RobotIPTextField3.setText(bis.readLine());
            RobotIPTextField4.setText(bis.readLine());

            bis.close();

        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
