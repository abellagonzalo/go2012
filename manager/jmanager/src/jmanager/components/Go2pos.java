	private void FieldLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FieldLabelMouseClicked
		double mx = (double)evt.getX()/ (double)FieldLabel.getWidth();
		double my =  (double)evt.getY()/ (double)FieldLabel.getHeight();
		double mxa;

		mx = 500.0*((mx -0.5)*2.0);
		my = 500.0*((my -0.5)*2.0);

		mxa = mx;
		mx = -my;
		my = -mxa;

		if (ActivationCheckBox.isSelected()) {
			JmanagerView.root.getConnection().go2posPrx.setGoalPos(mx, my);
			//dataResp resp = JmanagerView.root.getConnection().send("Go2pos:setPos:"+mx+":"+my+":");
			//System.out.println("("+mx+", "+my+")");
		}else
			System.out.println("("+mx+", "+my+")");
	}//GEN-LAST:event_FieldLabelMouseClicked
